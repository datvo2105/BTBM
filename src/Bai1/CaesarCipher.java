    private void btnActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionActionPerformed
        String msg = txtMessage.getText();
        int key = (Integer) spnKey.getValue();
        if (msg.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui nhập thông điệp");
        } else {
            msg = msg.toUpperCase();
            if (rdoEncrypt.isSelected()) {
                ecryptMessage(msg, key);
            } else if (rdoDecrypt.isSelected()) {
                decryptMessage(msg, key);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn hành động");
            }
        }


    }//GEN-LAST:event_btnActionActionPerformed

