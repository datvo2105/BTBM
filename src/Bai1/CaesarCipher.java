    private char ecryptChar(char c, int key) {
        if (Character.isLetter(c)) {
            return (char) ('A' + (c - 'A' + key) % 26);
        } else {
            return c;
        }
    }

    private char decryptChar(char c, int key) {
        if (Character.isLetter(c)) {
            return (char) ('A' + (c - 'A' - key) % 26);
        } else {
            return c;
        }
    }

    private void ecryptMessage(String msg, int key) {
        String result = "";
        for (int i = 0; i < msg.length(); i++) {
            result += ecryptChar(msg.charAt(i), key);
        }
        txtResult.setText(result);
    }

    private void decryptMessage(String msg, int key) {
        String result = "";
        for (int i = 0; i < msg.length(); i++) {
            result += decryptChar(msg.charAt(i), key);
        }
        txtResult.setText(result);
    }

